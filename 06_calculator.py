import math

a = float(input("Type the first number: "))
b = float(input("Type the second number: "))

sum = a + b
sub = a - b
mult = a * b
div = a / b
sqr = math.sqrt(a)

print("sum = " + str(sum))
print("sub = " + str(sub))
print("mult = " + str(mult))
print("div = " + str(div))
print("sqr = " + str(sqr))